// Language Manager Class
class LanguageManager {
    constructor() {
        this.currentLanguage = localStorage.getItem('selectedLanguage') || 'en';
        this.init();
    }

    init() {
        // Set initial language
        this.setLanguage(this.currentLanguage);
        this.setupLanguageButtons();
    }

    // Get translated text
    t(key) {
        return translations[this.currentLanguage][key] || translations['en'][key] || key;
    }

    // Set language and update UI
    setLanguage(lang) {
        if (lang !== 'en' && lang !== 'ur') return;
        
        this.currentLanguage = lang;
        localStorage.setItem('selectedLanguage', lang);
        
        // Set HTML direction and language
        document.documentElement.lang = lang;
        document.documentElement.dir = lang === 'ur' ? 'rtl' : 'ltr';
        document.body.dir = lang === 'ur' ? 'rtl' : 'ltr';
        
        // Add language class to body
        document.body.classList.remove('lang-en', 'lang-ur');
        document.body.classList.add(`lang-${lang}`);
        
        // Translate all elements with data-i18n attribute
        this.translateElements();
        
        // Update all input placeholders
        this.updatePlaceholders();
        
        // Trigger language change event
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
    }

    // Translate all elements with data-i18n attribute
    translateElements() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                if (el.hasAttribute('placeholder')) {
                    el.placeholder = this.t(key);
                }
            } else {
                el.textContent = this.t(key);
            }
        });

        // Translate data-i18n-title attributes
        document.querySelectorAll('[data-i18n-title]').forEach(el => {
            const key = el.getAttribute('data-i18n-title');
            el.title = this.t(key);
        });

        // Translate data-i18n-html attributes
        document.querySelectorAll('[data-i18n-html]').forEach(el => {
            const key = el.getAttribute('data-i18n-html');
            el.innerHTML = this.t(key);
        });
    }

    // Update all input placeholders
    updatePlaceholders() {
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            el.placeholder = this.t(key);
        });
    }

    // Setup language toggle buttons
    setupLanguageButtons() {
        const translateBtn = document.getElementById('translateBtn');
        if (translateBtn) {
            translateBtn.addEventListener('click', () => {
                const newLang = this.currentLanguage === 'en' ? 'ur' : 'en';
                this.setLanguage(newLang);
                this.updateTranslateBtnText();
            });
            this.updateTranslateBtnText();
        }

        // Setup language selector if exists
        const langSelector = document.getElementById('langSelector');
        if (langSelector) {
            langSelector.addEventListener('change', (e) => {
                this.setLanguage(e.target.value);
            });
            langSelector.value = this.currentLanguage;
        }
    }

    // Update translate button text
    updateTranslateBtnText() {
        const translateBtn = document.getElementById('translateBtn');
        if (translateBtn) {
            const buttonText = this.currentLanguage === 'en' ? 'اردو' : 'English';
            translateBtn.innerHTML = `<i class="fas fa-language"></i> ${buttonText}`;
        }
    }

    // Get all translations for a language
    getLanguageTranslations(lang = this.currentLanguage) {
        return translations[lang];
    }
}

// Initialize language manager when DOM is ready
let i18n = null;
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => {
        i18n = new LanguageManager();
    });
} else {
    i18n = new LanguageManager();
}
